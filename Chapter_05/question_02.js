let a = parseInt(prompt("Enter 1st number = "));
let b = parseInt(prompt("Enter 2nd number = "));

// subtraction:
let subtraction = a - b;
document.write(
  "The subtraction of " + a + " and " + b + " is " + subtraction + "<br>"
);

// multiplication:
let multiplication = a * b;
document.write(
  "The Multiplication of " + a + " and " + b + " is " + multiplication + "<br>"
);

// Modulus:
let modulus = a % b;

document.write("The Modulus of " + a + " and " + b + " is " + modulus);
